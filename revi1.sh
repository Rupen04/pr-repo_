# Linux directory structure :-
# 1>/ - Root directory
# 2>/bin - Essential command binaries
# 3>/boot - Boot loader files
# 4>/dev - Device files
# 5>/etc - System configuration files
# 6>/home - User home directories
# 7>/lib - Essential shared libraries
# 8>/media - Removable media
# 9>/mnt - Temporary mount points
# 10>/opt - Optional software packages
# 11>/proc - Process information
# 12>/root - Root user's home directory
# 13>/sbin - System binaries
# 14>/tmp - Temporary files
# 15>/usr - User utilities and applications
# 16>/var - Variable data files 

